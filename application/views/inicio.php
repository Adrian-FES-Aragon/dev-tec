    <h1 class="text-center">INTRODUCCIÓN</h1>
    <p class=" text fs-5 ">
        En este proyecto de reingeniería se menciona el proceso de reingeniería tanto de recuperación como de fabricación de equipos de cómputo y así aumentar la calidad en distintos sectores/áreas y disminuir el costo de producción.
        Como se menciona, veremos cómo es que llegan a ser los procesos de recuperación y fabricación de los equipos para así aumentar la producción de los mismo como también ejecutar los procesos de una manera correcta.
    </p>
