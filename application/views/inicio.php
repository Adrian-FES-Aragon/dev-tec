    <h1 class="text-center">OBJETIVO</h1>
    <p class=" text fs-5 ">
        El objetivo general es informar que es la Reingeniería, su origen e implementación para los equipos de cómputo y como su función en las empresas para su competitividad junto otras de las mismas áreas y evitar así en caer en lo anticuado y/o implementarlo de una manera errónea.
